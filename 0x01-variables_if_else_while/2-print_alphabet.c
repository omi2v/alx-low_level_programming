#include <stdlib.h>
#include <time.h>
#include <stdio.h>
/**
 * main - main block
 * Description: get a random number and print the number
 * if it is positive, negative or zero
 * Return: 0
 */
int main(void)
{
	int ch;
	for (ch = 'a'; ch <= 'z'; ++ch)
		putchar (ch);
	putchar ('\n');
	return (0);
}
